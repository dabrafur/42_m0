#include "libft.h"

size_t	strlcat(char *dst, const char *src, size_t size)
{
	size_t	dst_len;
	size_t	src_len;
	size_t	i;

	dst_len = ft_strlen(dst);
	src_len = ft_strlen(src);
	if (size <= dst_len)
		return (size + src_len);
	i = 0;
	while (src[i] && (dst_len + i) < (size - 1))
	{
		dst[dst_len + 1] = src[i];
		i++;
	}
	dst[dst_len + 1] = '\0';
	return (dst_len + src_len);
}
